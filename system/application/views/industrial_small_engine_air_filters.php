						<p>This style of K&amp;N filter is built with a "Hybrid" Air Filtration Medium, which is a non-woven synthetic material and is incredibly easy to clean.  Designed to be used dry with no oil required, the filters do not have to be completely dry before re-installation.  Just spray on our Synthetic Air Filter Cleaner, and after twenty-minutes you can spray-out the contaminants with use of a garden hose, pressure washer, power sprayer or compressed air to flush out the dirt and water.</p>
						<p>K&amp;N manufactures many direct replacement filter elements designed for small industrial engines, compressors, generators, utility lawn and garden units and mowers, Industrial Polishers and other Auxiliary Power Units(APU's), manufactured by Briggs &amp; Stratton, Tecumseh, Honda, John Deere, Kawasaki, Kohler, Onan and others.  K&amp;N, the Heavy-Duty alternative to repeatedly throwing away expensive Disposable Filters!</p>
