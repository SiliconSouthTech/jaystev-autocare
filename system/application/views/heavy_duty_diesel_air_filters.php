									<h4><strong>K&N Heavy Duty Air Filter Tech Talk</strong></h4>
									<h6>
										All HD filters start with "38" and have either an "S" or "R" suffix. The 38 signifies the filter is a K&N HD air filter. The "S" and "R" at the end of the part number (i.e. 38-2001S or 38-2001R) signifies which way the air flows through the media. A filter with the "S" suffix, should be red on the outside and white on the inside, flowing the air from the outside inward, aka Standard. Whereas the "R" suffix filters, which should be white on the outside, will flow air from the inside outward, aka Reversed. The red color is to signify the direction of air flow because the media is designed to flow in one direction. K&N does not recommend using the filter to flow air in the opposite direction than designed.
